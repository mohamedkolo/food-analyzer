# نرتب المفاتيح من الأطول للأقصر عشان "بطاطا حلوة" تتطابق قبل "بطاطا"
_KEYS_SORTED = sorted(FOOD_MEASURES.keys(), key=len, reverse=True)

# الأرقام + الوحدة (جم / جرام / غ / g / مل)
_QTY_RE = re.compile(r'(\d+)\s*(?:جم|جرام|غ|g|مل)')


def _match_food(item):
    """يلاقي أطول اسم أكلة موجود في الصنف."""
    for kw in _KEYS_SORTED:
        if kw in item:
            return FOOD_MEASURES[kw]
    return None


def _phrase(grams, food):
    """يحوّل الجرامات لجملة مكيال زي: ≈ كوب ونص"""
    g = food["g"]
    step = food.get("step", 0.25)
    if not g:
        return ""
    raw = grams / float(g)
    n = round(raw / step) * step
    n = round(n, 2)
    if n <= 0:
        n = step

    sing, dual, plur = UNIT_FORMS[food["unit"]]
    whole = int(n)
    frac = round(n - whole, 2)
    fracword = FRAC.get(frac, "")

    # أقل من وحدة
    if n < 1:
        return "≈ " + (FRAC.get(n, "") + " " + sing).strip()
    # وحدة واحدة بالظبط
    if abs(n - 1) < 0.01:
        return "≈ " + sing
    # بين 1 و 2
    if n < 2:
        return "≈ " + sing + (" و" + fracword if fracword else "")
    # اتنين بالظبط
    if abs(n - 2) < 0.01:
        return "≈ " + dual
    # بين 2 و 3
    if n < 3:
        return "≈ " + dual + (" و" + fracword if fracword else "")
    # 3 أو أكتر
    return "≈ " + str(int(round(n))) + " " + plur


def _annotate_item(item):
    food = _match_food(item)
    if not food:
        return item

    m = _QTY_RE.search(item)
    if m:
        # فيه جرامات → نحسب المكيال بالظبط ونحطه بعد الرقم
        try:
            grams = int(m.group(1))
        except Exception:
            return item
        phrase = _phrase(grams, food)
        if not phrase:
            return item
        return item[:m.end()] + f" ({phrase})" + item[m.end():]
    else:
        # مفيش جرامات → نحط مكيال تقريبي (وحدة واحدة) في آخر الصنف
        sing = UNIT_FORMS[food["unit"]][0]
        stripped = item.rstrip()
        trail = item[len(stripped):]
        if not stripped:
            return item
        return stripped + f" (≈ {sing})" + trail


def annotate(text):
    """الدالة الرئيسية: بتاخد نص الوجبة وترجّعه وجنب كل أكلة مكيالها."""
    if not text:
        return text
    parts = re.split(r'(\+)', str(text))
    out = []
    for p in parts:
        if p == "+" or not p.strip():
            out.append(p)
        else:
            out.append(_annotate_item(p))
    return "".join(out)

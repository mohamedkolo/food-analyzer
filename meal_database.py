def get_meal_pool(goal, culture):
    pools = {
        "weight_loss": WEIGHT_LOSS,
        "muscle_gain": MUSCLE_GAIN,
        "bulking": BULKING,
        "maintenance": MAINTENANCE,
    }
    pool = pools.get(goal, MAINTENANCE)
    if culture not in pool:
        culture = "مصري"
    return pool.get(culture, pool.get("مصري", {}))



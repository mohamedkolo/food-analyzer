def _contains_unsafe(meal_text, condition_key):
    unsafe_list = UNSAFE_FOODS.get(condition_key, [])
    for unsafe in unsafe_list:
        if unsafe in meal_text:
            return True
    return False


def filter_by_conditions(meals, conditions):
    if not conditions:
        return meals

    condition_map = {
        "قولون عصبي": "قولون",
        "سكري النوع الثاني": "سكري",
        "سكري النوع الاول": "سكري",
        "ضغط الدم المرتفع": "ضغط",
        "امراض القلب": "قلب",
        "الفشل الكلوي المزمن": "كلوي",
        "الحمل": "حامل",
        "الرضاعة الطبيعية": "حامل",
        "G6PD": "g6pd",
        "نقص G6PD": "g6pd",
        "ثلاسيميا": "ثلاسيميا",
    }

    active_conditions = []
    for c in conditions:
        key = condition_map.get(c)
        if key and key not in active_conditions:
            active_conditions.append(key)

    if not active_conditions:
        return meals

    result = []
    for meal in meals:
        meal_text = meal.get("meal", "")
        unsafe_for = None
        for cond_key in active_conditions:
            if _contains_unsafe(meal_text, cond_key):
                unsafe_for = cond_key
                break

        if unsafe_for:
            alternatives = SAFE_ALTERNATIVES.get(active_conditions[0], [])
            if alternatives:
                alt_idx = len(result) % len(alternatives)
                result.append(alternatives[alt_idx])
            else:
                result.append(meal)
        else:
            result.append(meal)

    return result


